const mongoose = require('mongoose');

//Test schema for database
const UserSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    }

});

module.exports = User = mongoose.model('User', UserSchema);

